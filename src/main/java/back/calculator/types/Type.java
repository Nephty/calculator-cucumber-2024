package back.calculator.types;

/**
 * Enumeration of the types of values that can be used in the calculator.

 */
public enum Type {
    /**
     * Integer value. Example: 1
     */
    INT,
    /**
     * Real value. Example: 1.0
     */
    REAL,
    /**
     * Rational value. Example: 1/2
     */
    RATIONAL
}
